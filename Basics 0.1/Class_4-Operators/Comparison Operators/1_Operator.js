const a = 15, b = 7, c = 'NASIM';
// Equal to Operator
console.log(a == 15);
console.log(b == 10);
console.log(c == 'NASIM');

const d = 55;
// strict equal operator
console.log(d === 55);
console.log(d === '55');

const E = 5, F = 'Hellos';
// not equal operator
console.log(E != 10);
console.log(F != 'HELLos');

const e = 7, f = 'HELLO';
// strict not equal operator
console.log(e !== 7);
console.log(f !== 'Hello');

const A = 7;
// greater than operator
console.log(A > 5);
console.log(A > 10);

const B = 3;
// greater than or equal operator
console.log(B >= 3);

const C = 3, D = 2;
// less than operator
console.log(C < 2); // false
console.log(D < 3); // true

const E = 2;
// less than or equal operator
console.log(E <= 5); // true
console.log(E <= 2); // true

